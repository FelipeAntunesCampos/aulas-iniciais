// A variavel opção é setada.
let opcao = 1
// sistema de switch case com as opções
switch (opcao) {
    case 1:
        console.log("Opção 1 selecionada: Verificar saldo.");
    
        break;
        case 2: 
        console.log("Opção 2 selecionada: Fazer saque.");

        break;
        case 3:
        console.log("Opção 3 selecionada: Fazer depósito.");
        
        break;
        case 4:
        console.log("Opção 4 selecionada: Consultar histórico.");

        break;
        default:
        console.log("Opção inválida. Por favor, escolha uma opção válida.");

        break;
}